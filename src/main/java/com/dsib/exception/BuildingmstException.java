package com.dsib.exception;

public class BuildingmstException extends RuntimeException {
    public BuildingmstException() {
        super();
    }

    public BuildingmstException(String message) {
        super(message);
    }

    public BuildingmstException(String message, Throwable cause) {
        super(message, cause);
    }

    public BuildingmstException(Throwable cause) {
        super(cause);
    }

    protected BuildingmstException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
